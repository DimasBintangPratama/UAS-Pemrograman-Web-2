        <h1 align="center">Sociolla</h1>
        <p style="text-align:justify">PT. Social Bella Indonesia atau Sociolla menduduki peringkat pertama pada daftar Linkedin Top Startups 2021, sebagai salah satu Startup potensial yang mampu menyerap investasi dan tenaga kerja dalam jumlah besar sepanjang tahun 2021. Sociolla kini  menjadi salah satu pemain kunci dalam bidang beauty e-commerce di Indonesia, yang juga telah mendirikan 25 offline store dengan teknologi omni channel di berbagai kota. Sebagai beauty company berbasis teknologi, Sociolla pun mencari karyawan yang fast, agile, positive, memiliki sense of ownership dan continuous improvement untuk memajukan industri kecantikan Indonesia.</p>
        <h4><b>Profil Perusahaan Sociolla dan Sejarah Perjalanannya</b></h4>
        <p style="text-align:justify">Kebutuhan akan kosmetik merupakan salah satu kebutuhan primer, khususnya bagi kaum hawa. Berbagai produk kosmetik penting untuk dimiliki sebagai penunjang penampilan. Hal ini sesuai dengan definisi kosmetik yang dibahas dalam buku berjudul Buku Ajar Teknologi Kosmetik yang disusun oleh Dina Rahmawanty Dan Destria Indah Sari (2019: 81). Dalam buku tersebut dijelaskan bahwa kosmetik adalah suatu bahan untuk mempercantik diri, dahulu diramu dari bahan-bahan alami di sekitar kehidupan manusia, tetapi sekarang dibuat manusia tidak hanya dari bahan alami, melainkan juga dari bahan buatan untuk maksud meningkatkan kecantikan.</p>
        <p style="text-align:justify">Di samping kebutuhan kosmetik, mayoritas kaum hawa juga membutuhkan produk perawatan kulit atau yang juga disebut dengan skin care. Produk skin care digunakan untuk menjaga kondisi kulit agar tetap sehat. Sebagaimana yang dibahas dalam buku berjudul Rahasia Cantik Perempuan Asia yang disusun oleh Siti Nur Aidah dan Tim Penerbit KBM Indonesia (2021: 135). Tertulis dalam buku tersebut bahwa skincare merupakan rangkaian kegiatan  perawatan kulit yang mendukung kesehatan dan kecantikan kulit. Pembersih wajah, pelembap, hingga tabir surya adalah berbagai produk perawatan kulit atau skincare yang umum digunakan. Manfaat menggunakan skincare adalah dapat membantu menjaga kesehatan kulit secara keseluruhan sekaligus memperbaiki masalah kulit yang dialami seperti jerawat, bintik hitam, keriput, dan lainnya.</p>
        <p style="text-align:justify">Semakin banyaknya kebutuhan skincare dan kosmetik ini rupanya mendorong Chrisanti Indiana untuk membuat platform khusus yang memudahkan kaum hawa untuk dapat membeli produk kecantikan dan skin care sesuai dengan kebutuhan. Platform ini bernama Sociolla. Sociolla adalah perusahaan e-commerce sekaligus platform sosial yang berfokus kepada kecantikan dan perawatan kulit. Platform yang didirikan sejak tahun 2015 ini menyediakan layanan berbagai fitur yang memudahkan Anda untuk membeli produk kosmetik dan skin care terkenal. Tak hanya itu di sini Anda juga dapat berbagai review produk kecantikan dengan testimoni yang jujur.</p>
