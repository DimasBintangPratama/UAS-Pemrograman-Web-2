        <h1 align="center">Gojek</h1>
        <p style="text-align:justify">Startup decacorn ini memang menjadi perusahaan idaman para lulusan IT. Sebagai perusahaan transportasi online, tentu membutuhkan banyak talenta digital untuk mengembangkan bisnisnya. Bahkan, Gojek secara rutin mengadakan berbagai program training maupun magang untuk mahasiswa dan fresh graduate jurusan IT atau engineering seperti Upscale, Engineering Bootcamp dan Go Squad Tech. Di program-program tersebut, para peserta akan menerima pelatihan seputar Data Science, Product Engineering, Software Engineering, Business Intelligence dan sebagainya. Untuk kalian lulusan Teknik Informatika yang memiliki skill mumpuni, kreatif, berinisiatif tinggi, serta siap untuk menghadapi ritme kerja yang serba cepat, Gojek mungkin bisa menjadi pilihan yang tepat.</p>
        <h4><b>Sejarah PT Gojek Indonesia</b></h4>
        <p style="text-align:justify">Seperti yang telah dibahas sebelumnya, PT Gojek Indonesia berdiri pada 2010. Pada saat itu PT Gojek Indonesia hanya menyediakan layanan transportasi berupa ojek. Dimulai dengan 20 ojek dan 1 call center. Di awal berdirinya, Gojek belum menggunakan aplikasi seperti sekarang ini. Baru pada tahun 2015 Aplikasi Gojek dibuat. Berkat adanya aplikasi, ada kenaikan pesanan dari 3.000 per hari jadi 10.000 per hari. Setialah hanya beroperasi di Jakarta saja, PT Gojek Indonesia kemudian melakukan ekspansi ke luar kota. Di tahun ini pula, PT Gojek Indonesia menambah layanannya ke ranah pesan antar makanan, penjualan tiket, dll.</p>
        <p style="text-align:justify">Pada tahun 2017, Gojek menjadi perusahaan unicorn pertama di Indonesia. Di tahun ini, PT Gojek mengalami kenaikan pesanan menjadi 300,000 per hari. Gojek juga memberikan kesempatan pada lebih dari 30,000 teman difabel untuk bergabung. Pada tahun 2017, PT Gojek Indonesia berada di peringkat 17 di antara 20 perusahaan yang mengubah dunia versi Fortune, usai mengalami pertumbuhan 3.600 kali lipat dalam 18 bulan, salah satu pertumbuhan tercepat di dunia. Pada 2018, PT Gojek Indonesia melakukan ekspansi binis ke luar negeri, yakni ke Vietnam dan Thailand, yang membuat perusahaan ini terus mengalami pertumbuhan.</p>
        <p style="text-align:justify">Pada 2020, aplikasi Gojek di Indonesia, Singapura, Thailand, dan Vietnam digabung menjadi satu, di mana Aplikasi Gojek diunduh lebih dari 190 juta kali, dan memiliki peran penting selama pandemi Covid-19. Pada 2021, Gojek dan Tokopedia bergabung menjadi GoTo, perusahaan teknologi terbesar di Indonesia dan menjadi ekosistem "go to" untuk memenuhi kebutuhan sehari-hari.</p>
        <h4><b>Produk PT Gojek Indonesia</b></h4>
        <p style="text-align:justify">PT Gojek Indonesia dimulai dengan satu layanan transportasi, yakni dengan ojek online. Namun sekarang, sudah ada lebih dari 20 produk dan layanan, yang terbagi dalam lima kategori, yakni sebagai berikut:</p>
        <h5>1. <b>Transport & Logistik</b></h5>
        <ul style="list-style-type: lower-alpha">
            <li><b>GoRide</b>. GoRide merupakan layanan transportasi dengan sepeda motor alias ojek.</li><br>
            <li><b>GoCar</b>. GoCar adalah layanan transportasi dengan mobil.</li><br>
            <li><b>GoSend</b>. GoSend adalah layanan pengiriman barang secara instan dengan cakupan wilayah dalam kota.</li><br>
            <li><b>GoBox</b>. GoBox adalah layanan pengiriman barang dalam jumlah besar dengan menggunakan kendaraan roda empat. Tidak seperti gosend, layanan ini tidak menerapkan jarak maksimal. Mengirim barang bisa lebih dari 100 KM pada area layanan GoBox. Wilayah GoBox antara lain Jabodetabek, Bandung, Yogyakarta, Semarang, Surabaya, Bali, Medan, Palembang, Balikpapan., Makassar, Malang, Solo, Karawang, Tasikmalaya, Serang, dan Cirebon.</li><br>
            <li><b>GoBlueBird</b>. Ini adalah layanan pemesanan taksi Blue Bird. Lewat GoBlueBird, pelanggan bisa mengakses puluhan ribu armada Blue Bird di seluruh Indonesia.</li><br>
        </ul>
        <h5>2. <b>Pembayaran</b></h4>
        <ul style="list-style-type: lower-alpha">
            <li><b>GoPay</b>. GoPay adalah dompet digital serba bisa, mulai dari transaksi cepat untuk semua layanan Gojek dan ratusan Rekan Usaha, hingga mengirim atau menerima uang dengan mudah, semua bebas dilakukan bersama GoPay.</li><br>
            <li><b>GoTagihan</b>. GoTagihan adalah layanan untuk membayar agihan air, BPJS, gas, internet, listrik, tv kabel.</li><br>
            <li><b>GoPayLater</b>. GoPayLater dari Findaya adalah metode pembayaran yang merupakan bagian dari GoTo Financial. Segala urusan mulai dari bayar tagihan, beli makanan sampai pakaian, semua bisa kamu penuhi di layanan Gojek, Tokopedia hingga Rekan Usaha GoPay online, dan offline kapan aja bayarnya abis gajian.</li><br>
            <li><b>GoGive</b>. GoGive adalah layanan yang memungkinkan pelanggan untuk menyalurkan bantuannya.</li><br>
            <li><b>GoSure</b>. GoSure adalah produk asuransi barang-barang.</li><br>
            <li><b>GoInvestasi</b>. GoInvestasi adalah cara termurah dan termudah untuk berinvestasi dan membangun kebiasaan menabung.</li><br>
            <li><b>GoCorp</b>.  GoCorp adalah layanan yang memudahkan perusahaan untuk memberikan fasilitas transportasi karyawan melalui aplikasi Gojek yang dapat dikelola limit pemakaiannya. Mode transportasi dengan jutaan mitra pengemudi GoCar, GoCar Protect+ dan GoRide siap melayani kebutuhan mobilitas karyawan.</li><br>
        </ul>
        <h5>3. <b>Pesan Makanan dan Belanja</b></h4>
        <ul style="list-style-type: lower-alpha">
            <li><b>GoFood</b>. GoFood adalah layanan di mana pelanggan bisa memesan makan dan di antar ke tujuan.</li><br>
            <li><b>GoMart</b>. GoMart adalah layanan di mana pelanggan dapat berbelanja kebutuhan sehari-hari tanpa perlu keluar rumah, cukup pilih item belanjaan, bayar, lalu tunggu belanjaan diantar sampai rumah.</li><br>
            <li><b>GoMed</b>. GoMed adalah layanan yang memudahkan akses ke layanan berkualitas yang disediakan oleh dokter asli dan lembaga kesehatan terpercaya bagi pengguna Gojek di Indonesia.</li><br>
            <li><b>GoShop</b>. GoShop adalah salah satu produk Gojek yang memungkinkan Anda membeli hampir semua hal, dari mana saja.</li><br>
        </ul>
        <h5>4. <b>Hiburan</b></h4>
        <ul style="list-style-type: lower-alpha">
            <li><b>GoPlay</b>. GoPlay merupakan layanan streaming yang memungkinkan penggunanya untuk menikmati berbagai jenis hiburan.</li><br>
            <li><b>GoTix</b>. GoTix adalah layanan pemesanan tiket bioskop secara online.</li><br>
        </ul>
        <h5>5. <b>Bisnis</b></h4>
        <ul style="list-style-type: lower-alpha">
            <li><b>GoBiz</b>. GoBiz adalah super app dari Gojek yang diperuntukkan khusus para pebisnis di Indonesia. Dari kelola pesanan GoFood dan di toko, terima pembayaran GoPay, analisa laporan penjualan, sampai buat promo; semua bisa dilakukan di aplikasi GoBiz.</li><br>
            <li><b>Midtrans</b>. Midtrans adalah layanan menerima pembayaran online dan offline. Dengan 24 metode pembayaran online dan offline, pelanggan Anda dapat memilih untuk menggunakan e-money, kartu kredit/debit, e-banking, atau pembayaran tunai di gerai.</li><br>
            <li><b>Moka</b>. Moka adalah layanan yang menyediakan berbagai solusi untuk kebutuhan penjualan offline & online. Moka memiliki sistem POS untuk toko offline serta memungkinkan Anda untuk membangun toko online pribadi melalui GoStore.</li><br>
            <li><b>Selly</b>. Selly merupakan keyboard lengkap untuk bantu kamu para penjual online. Mulai dari cek ongkir, balas chat dengan auto-text, hingga pesan kurir & layani pembayaran, semua bisa dilakukan tanpa perlu pindah-pindah aplikasi.</li><br>
        </ul>
