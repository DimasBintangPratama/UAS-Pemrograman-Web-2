        <h1 align="center">Google Indonesia</h1>
        <p style="text-align:justify">Siapa sih, yang gak kenal Google, search engine paling populer di Indonesia bahkan dunia? Hingga kini, Google memiliki 70 kantor yang tersebar di 50 negara. Salah satunya, Indonesia. Seperti halnya kantor pusat Google di California, kantor Google Indonesia pun memiliki banyak fasilitas yang membuat karyawannya nyaman dan betah bekerja. Tak heran, perusahaan ini pun menjadi salah satu incaran para fresh graduate bidang teknologi untuk mengembangkan karir. Sebagai perusahaan IT terbesar di dunia, Google tentu selalu membutuhkan para ahli teknologi handal untuk selalu keep up dengan tren teknologi terkini dan memberikan user experience terbaik untuk para penggunanya.</p>
        <h4><b>Biografi</b></h4>
        <p style="text-align:justify">Google Inc. adalah perusahaan multinasional Amerika Serikat yang bergerak di bidang layanan dan produk internet seperti mesin pencari, komputasi web, perangkat lunak, dan periklanan daring. Perusahaan ini didirikan oleh alumni Stanford University yaitu Larry Page dan Sergey Brin. Pada tahun 1996, mereka membangun sebuah mesin pencari (awalnya bernama BackRub) yang menggunakan link untuk menentukan pentingnya halaman web individu. Larry dan Sergey hanya 16% dari saham perusahaan, maka pada tanggal 4 September 1998, mereka membuat Google Inc. sebagai perusahaan swasta. Namun pada tanggal 19 Agustus 2004, perusahaan kemudian berubah menjadi perusahaan publik dengan sahamnya terdaftar di NASDAQ. Kini, markas Google berada di 1600 Amphitheatre Parkway, Mountain View, CA 94043.</p>
        <p style="text-align:justify">Proyek Google dimulai pada bulan Januari 1996 sebagai sebuah proyek penelitian oleh Larry Page dan Sergey Brin ketika mereka berdua masih menjadi mahasiswa PhD di Stanford University di Stanford, California. Mesin pencari konvensional umumnya menghitung berapa kali istilah pencarian suatu web muncul pada halaman, namun kedua penemu Google ini menemukan teori lain yang lebih baik untuk menganalisis website. Mereka menganalisis hubungan antar wesbsites yang kemudian disebut dengan teknologi PageRank. Teknologi ini menentukan relevansi sebuah website oleh jumlah halaman, dan pentingnya halaman tersebut, yang menghubungkan kembali ke situs asli.</p>
        <p style="text-align:justify">Larry dan Sergey awalnya menjuluki mesin pencari mereka dengan nama 'BackRub' karena sistemnya yang mengecek banklink untuk memperkirakan pentingnya sebuah situs. Kemudian mereka berganti nama menjadi Google yang sebenarnya berasal dari ejaan  kata "googol". Sebagai sebuah proyek akademis, Google awalnya dijalankan oleh website Stanford University'dengan domain google.stanford.edu dan z.stanford.edu. Google kemudian diregistrasikan sebagai sebuah perusahaan pada tanggal 15 September 1997 meskipun berdirinya pada tanggal 4 September 1998. Perusahaan ini menjadikan garasi Susan Wojcicki, teman Larry dan Sergey, yang terletak di Menlo Park, California sebagai markas mereka. Craig Silverstein, sesama mahasiswa PhD di Stanford dipekerjakan sebagai karyawan pertama Google.</p>
        <p style="text-align:justify">Pada Mei 2011, jumlah pengunjung bulanan ke Google melampaui satu miliar untuk pertama kalinya, meningkat 8,4 persen dari Mei 2010 (931.000.000). Pada Januari 2013, Google mengumumkan telah memperoleh $ 50 miliar dalam pendapatan tahunan di tahun 2012. Penawaran umum perdana Google (IPO) terjadi pada tanggal 19 Agustus 2004. Pada saat itu Larry Page, Sergey Brin, dan Eric Schmidt setuju untuk bekerja sama di Google selama 20 tahun, sampai tahun 2024. Perusahaan ini menawarkan 19.605.052 saham dengan harga $ 85 per saham. Saham dijual dalam format lelang online yang unik menggunakan sistem yang dibangun oleh Morgan Stanley dan Credit Suisse sebagai penjamin kesepakatan emisi. Mayoritas saham Google masih dalam kendali perusahaan sehingga banyak banyak karyawan Google menjadi milyuner mendadak. Salah satu kompetetitor Google, Yahoo, yang memiliki saham 8,4 juta lembar saham Google sebelum IPO terjadi juga mendapat banyak keuntungan.</p>
        <p style="text-align:justify">Dengan tingginya nilai jual saham Google, banyak orang mengisukan perubahan company culture dan filosofi anti-corporate dan no evil-nya. Meskipun Larry dan Sergey berjanji bahwa pada laporan untuk investor potensialnya IPO tidak akan merubah apa pun, namun berbagai artikel seperti The New York Times mulai menyoroti perubahan itu. Maka, untuk mencegah perubahan budaya dalam perusahaan, Google secara khusus membuat Chief Culture Officer yang bertugas yang melakukan tugas HRD Google. Chief Culture Officer dimaksudkan untuk mengembangkan dan mempertahankan budaya dan pekerjaan tetap pada jalan yang benar menuju nilai-nilai inti perusahaan.</p>
