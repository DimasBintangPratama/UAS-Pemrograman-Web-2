        <h4><b>Sejarah Tokopedia</b></h4>
        <p style="text-align:justify">Bagian paling menarik dari profil perusahaan Tokopedia adalah latar belakang dan kisah perjalanannya. Bisnis selalu berawal dari permasalahan. Hal yang sama juga terjadi di Tokopedia. Kontur kepulauan Indonesia bak pedang bermata dua. Di satu sisi, 17 ribu pulau dari Sabang sampai Merauke merupakan sumber daya alam yang bernilai ekonomi tinggi. Tapi di sisi lain, kontur kepulauan menjadi tantangan pemerintah dalam mengupayakan pemerataan ekonomi. Demi mendapatkan pasar yang lebih baik, para pedagang lebih fokus mendistribusikan produknya ke kota-kota besar. Ekonomi pun terpusat di titik-titik urban. Sementara kawasan tepi harus menghadapi kesulitan ekonomi, ketertinggalan pembangunan, kurangnya fasilitas publik, dan hal-hal lain sebagai dampak dari ekonomi yang tidak merata.</i></p>
        <center><img src="img/bahan isi tokopedia.png" alt="" class="img-responsive" width="500" height="250"></center><br>
        <p style="text-align:justify">Berangkat dari keprihatinan terhadap kondisi ini, William Tanuwijaya dan Alpha Edison pun mulai memutar otak. Pengalaman hidup para Founding Father merangsang mereka untuk memperpendek arus distribusi dan memotong tali kesenjangan antar daerah. Perlu sebuah solusi untuk menyediakan produk yang bisa dijangkau oleh siapa saja, kapan saja, dan dari mana saja. Sebuah solusi dimana masyarakat kota kecil tidak harus pergi ke kota besar hanya untuk membeli barang  yang tidak tersedia di pasar lokal. William dan Alpha akhirnya menemukan solusinya. Tepat pada peringatan hari kemerdekaan Indonesia, 17 Agustus 2009, William Tanuwijaya dan Leontinus Alpha Edison meluncurkan Tokopedia dengan pemerataan ekonomi digital sebagai misi utama.</p>
        <p style="text-align:justify">Dalam 12 tahun pertamanya, Tokopedia mendorong masyarakat Indonesia untuk bersama-sama mengatasi kesenjangan. Para pedagang termotivasi untuk go online guna memperluas pasar sekaligus menyediakan lebih banyak opsi produk untuk masyarakat daerah. Konsumen pun mulai melirik Tokopedia sebagai tempat untuk mendapatkan hampir semua jenis barang berkualitas dengan harga terjangkau. Berkat Tokopedia, garis pemisah distribusi produk antara kota besar dan kota kecil seakan hilang begitu saja. Sekarang semua orang bisa membeli apa saja yang mereka inginkan hanya dengan sepasang ibu jari.</p>
        <h4><b>2009</b></h4>
        <p style="text-align:justify">Tokopedia memulai bisnis dalam bidang marketplace konsumen ke konsumen (C2C). Sampai sekarang, identitas inilah yang paling melekat dan paling banyak dikenal orang dari Tokopedia.</p>
        <h4><b>2014</b></h4>
        <p style="text-align:justify">Tokopedia menjadi perusahaan Indonesia pertama yang masuk dalam portofolio 2 investor raksasa, Softbank dan Sequoia. Lebih dari sekedar pendanaan ke perusahaan, ini merupakan prestasi nasional yang menjadi cikal bakal masuknya lebih banyak investor ke ekosistem startup Indonesia.</p>
        <h4><b>2015</b></h4>
        <p style="text-align:justify">Tokopedia menjadi pelopor pengiriman instan lewat kerjasama dengan perusahaan penyedia transportasi online. Berkat inisiatif ini, tren pengiriman barang online shopping berubah untuk selamanya.</p>
        <h4><b>2016</b></h4>
        <p style="text-align:justify">Setelah sukses menjalankan marketplace, Tokopedia mulai melebarkan sayap ke lini bisnis lain. Ekspansi awalnya adalah produk digital dan fintech.</p>
        <h4><b>2017</b></h4>
        <p style="text-align:justify">Perusahaan rintisan Jack Ma, Alibaba, mengucurkan investasi sebesar 1,1 miliar USD ke Tokopedia.</p>
        <h4><b>2018</b></h4>
        <p style="text-align:justify">Pendanaan terus mengalir masuk. Tokopedia kembali mendapatkan suntikan dana sebesar 1,1 miliar USD dari Alibaba Group dan Softbank Vision Fund.</p>
        <h4><b>2019</b></h4>
        <p style="text-align:justify">Produk dan layanan Tokopedia terus bertambah. Setahun sebelum pandemi, Tokopedia meluncurkan beberapa layanan baru, yaitu:</p>
        <ul style="list-style-type: disc">
            <li><b>TokoCabang</b>: layanan gudang pintar untuk penjual Tokopedia.</li><br>
            <li><b>Tokopedia Salam</b>: laman khusus produk islami Tokopedia.</li><br>
            <li><b>Tokopedia Play</b>: live streaming dan live shopping. </li><br>
        </ul>
        <h4><b>2020</b></h4>
        <p style="text-align:justify">Tokopedia meluncurkan halaman khusus untuk penjualan jasa bernama <b>Tokopedia Jasa</b>.</p>
        <h4><b>2021</b></h4>
        <p style="text-align:justify">Tokopedia merger dengan Gojek dan membentuk GoTo. Pada tahun ini pula Tokopedia NOW diluncurkan untuk memenuhi barang-barang kebutuhan masyarakat (sembako) dengan jaminan <b>stok pasti ada</b> dan <b>langsung kirim</b>.</p>
